
public class Ticket {

	String seatNumber;
	String price;
	String eventName;
	String date;
	String time;
	
	public Ticket(String seatNumber, String price, String eventName, String date, String time){
		this.seatNumber = seatNumber;
		this.price = price;
		this.eventName = eventName;
		this.date = date;
		this.time = time;
	}

}
